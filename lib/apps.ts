// Helper function to generate a logo initial from a name
const getLogoInitial = (name: string): string => {
  const parts = name.split(" ")
  if (parts.length > 1) {
    return `${parts[0][0]}${parts[1][0]}`.toUpperCase()
  }
  return name.substring(0, 2).toUpperCase()
}

export const apps: App[] = appsData.map((app) => {
  const liveUrl = dedicatedPages[app.name]
  const hasDedicatedPage = !!liveUrl

  // Normalize data to fit the App interface and UI components
  const normalizedApp: App = {
    id: app.id,
    name: app.name,
    vendor: app.vendor,
    description: app.description,
    features: app.features || app.tags || [],
    logo: app.logo || getLogoInitial(app.name),
    isFeatured: app.isFeatured || false,
    rating: app.rating || 4.0 + Math.random(), // Assign a default/random rating if missing
    category: app.category,
    hasDedicatedPage: app.hasDedicatedPage !== undefined ? app.hasDedicatedPage : hasDedicatedPage,
    liveUrl: app.liveUrl !== undefined ? app.liveUrl : liveUrl,
    contextualBenefit: app.contextualBenefit || "",
  }

  return normalizedApp
})

export function getAppById(id: string): App | undefined {
  return apps.find((app) => app.id === id)
}

export function getAppsByCategory(category: string): App[] {
  return apps.filter((app) => app.category === category)
}

export function getFeaturedApps(): App[] {
  return apps.filter((app) => app.isFeatured)
}
